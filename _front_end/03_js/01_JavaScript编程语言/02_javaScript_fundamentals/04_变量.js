// 使用let定义变量，并赋值
let v1 = "hello";
console.log("使用let定义了变量v1，并赋值为：" + v1);

//省略let声明
v2 = "hell02";
console.log("不在脚本中使用use strict声明启用严格模式,变量可以省略let声明：" + v2);

//使用var声明变量
var v3="hello3"
console.log("老旧的变量声明方式var定义变量v3："+v3)

//使用const定义常量
const c1 = "1982";
// c1 = "1983";  //不能对常量重新赋值
console.log("使用const定义常量c1,并赋值为：" + c1);

